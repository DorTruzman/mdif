import { mdif, mdifService } from './mdif'
import { ArgumentParser } from 'argparse'

const MDIF_PORT = 21020

const parser = new ArgumentParser({
    description: 'node.js MDIF demo',
})
parser.add_argument('host', { help: 'IP address' })
parser.add_argument('-p', '--port', { help: `TCP port (default ${MDIF_PORT})`, type: 'int', default: MDIF_PORT })
const args = parser.parse_args()
console.dir(args)

async function main() {
    await mdifService(args.host, args.port)
    mdif.on('getDeviceInfoRes', (res) => {
        console.log('Device has serial number', res.serialNumber)
        process.exit(0)
    })
    mdif.emit('getDeviceInfoReq', {})
}

// active timer keeps node from exiting
setTimeout(() => {
    console.log('No connection to/response from device')
    process.exit(1)
}, 5000)

main()
